const FALLBACK = G['?'] as readonly number[];

function glyphOf(ch: string): readonly number[] {
  return G[ch.toUpperCase()] ?? FALLBACK;
}

/** Pixel width of a string at scale 1 (1 px per font pixel, 1 px tracking). */
export function measurePixelText(text: string, tracking = 1): number {
  if (!text.length) return 0;
  return text.length * (GLYPH_W + tracking) - tracking;
}

export function drawPixelText(
  ctx: CanvasRenderingContext2D,
  text: string,
  x: number,
  y: number,
  scale: number,
  color: string,
  tracking = 1,
): void {
  ctx.fillStyle = color;
  let cx = x;
  for (const ch of text) {
    const rows = glyphOf(ch);
    for (let r = 0; r < GLYPH_H; r++) {
      const bits = rows[r] as number;
      for (let c = 0; c < GLYPH_W; c++) {
        if (bits & (1 << (GLYPH_W - 1 - c))) {
          ctx.fillRect(cx + c * scale, y + r * scale, scale, scale);
        }
      }
    }
    cx += (GLYPH_W + tracking) * scale;
  }
}

export interface PixelTextOptions {
  scale: number;
  color?: string;
  /** Vertical gradient stops [top, bottom] — overrides color. */
  gradient?: readonly [string, string];
  /** Hard drop shadow color (offset 1 font-pixel down-right). */
  shadow?: string;
  tracking?: number;
}

/** Render text to a canvas (device-pixel exact). Returns the canvas. */
export function pixelTextCanvas(text: string, opts: PixelTextOptions): HTMLCanvasElement {
  const tracking = opts.tracking ?? 1;
  const scale = Math.max(1, Math.round(opts.scale));
  const shadowPad = opts.shadow ? 1 : 0;
  const w = (measurePixelText(text, tracking) + shadowPad) * scale;
  const h = (GLYPH_H + shadowPad) * scale;
  const canvas = document.createElement('canvas');
  canvas.width = Math.max(1, w);
  canvas.height = Math.max(1, h);
  const ctx = canvas.getContext('2d') as CanvasRenderingContext2D;

  if (opts.shadow) {
    drawPixelText(ctx, text, scale, scale, scale, opts.shadow, tracking);
  }
  if (opts.gradient) {
    // Draw mask, then colorize through source-in compositing.
    const mask = document.createElement('canvas');
    mask.width = canvas.width;
    mask.height = canvas.height;
    const mctx = mask.getContext('2d') as CanvasRenderingContext2D;
    drawPixelText(mctx, text, 0, 0, scale, '#fff', tracking);
    mctx.globalCompositeOperation = 'source-in';
    const grad = mctx.createLinearGradient(0, 0, 0, GLYPH_H * scale);
    grad.addColorStop(0, opts.gradient[0]);
    grad.addColorStop(1, opts.gradient[1]);
    mctx.fillStyle = grad;
    mctx.fillRect(0, 0, mask.width, mask.height);
    ctx.drawImage(mask, 0, 0);
  } else {
    drawPixelText(ctx, text, 0, 0, scale, opts.color ?? '#e8e0cf', tracking);
  }
  return canvas;
}
